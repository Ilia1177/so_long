#include "../include/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*base;

	base = "0123456789";
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	write(fd, &base[n % 10], 1);
}
